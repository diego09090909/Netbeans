    private void jtbtn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtbtn_agregarActionPerformed
        int idMinuta = 0;
        String tipo, nombre, descripcion;
        String dia, mes, agnio, fechaStr;
        Date fecha = null;

        try {
            idMinuta = Integer.parseInt(this.jtxt_idMinuta.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID inválido", "Validación", JOptionPane.WARNING_MESSAGE);
            this.jtxt_idMinuta.requestFocus();
            return;
        }

        tipo = this.jcmb_tipo.getSelectedItem().toString();
        nombre = this.jtxt_nombre.getText();
        descripcion = this.jtxt_descripcion.getText();

        dia = this.jtxt_dia.getText();
        mes = this.jtxt_mes.getText();
        agnio = this.jtxt_agnio.getText();

        if (nombre.isEmpty() || descripcion.isEmpty() || dia.isEmpty() || mes.isEmpty() || agnio.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Completa todos los campos obligatorios", "Validación", JOptionPane.WARNING_MESSAGE);
            return;
        }

        fechaStr = dia + "/" + mes + "/" + agnio;
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        formato.setLenient(false);

        try {
            fecha = formato.parse(fechaStr);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Fecha inválida (dd/MM/aaaa)", "Validación", JOptionPane.WARNING_MESSAGE);
            this.jtxt_dia.requestFocus();
            return;
        }

        Minuta minuta = new Minuta();
        minuta.setIdMinuta(idMinuta);
        minuta.setTipo(tipo);
        minuta.setNombre(nombre);
        minuta.setDescripcion(descripcion);
        minuta.setFecha(fecha);

        MinutaPP reg = new MinutaPP();

        if (reg.insertar(minuta)) {
            JOptionPane.showMessageDialog(this, "Minuta agregada correctamente", "Ingreso Minuta", JOptionPane.INFORMATION_MESSAGE);
            this.limpiar();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo agregar la minuta", "Ingreso Minuta", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jtbtn_agregarActionPerformed

