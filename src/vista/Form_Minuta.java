    private void jbtn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_eliminarActionPerformed

        String idTexto = this.jtxt_idMinuta.getText().trim();

        if (idTexto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un ID para eliminar", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int opcion = JOptionPane.showConfirmDialog(this, "¿Seguro que deseas eliminar?", "Aviso", 1);

        if (opcion == JOptionPane.YES_OPTION) {
            int idMinuta;
            try {
                idMinuta = Integer.parseInt(idTexto);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "ID inválido", "Validación", JOptionPane.WARNING_MESSAGE);
                this.jtxt_idMinuta.requestFocus();
                return;
            }

            MinutaPP reg = new MinutaPP();
            boolean eliminado = reg.eliminar(idMinuta);

            if (eliminado) {
                JOptionPane.showMessageDialog(this, "Minuta eliminada correctamente", "Eliminado", JOptionPane.INFORMATION_MESSAGE);
                this.limpiar();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar la minuta", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Minuta NO eliminada", "Cancelado", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jbtn_eliminarActionPerformed

    private void jbtn_listarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_listarActionPerformed

        MinutaPP reg = new MinutaPP();
        DefaultTableModel modelo = (DefaultTableModel) this.jtbl_datos.getModel();
        modelo.setRowCount(0); // Limpiar tabla

        List<Minuta> lista = reg.listar();

        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");

        for (Minuta m : lista) {
            modelo.addRow(new Object[]{
                m.getIdMinuta(),
                m.getTipo(),
                m.getNombre(),
                m.getDescripcion(),
                m.getFecha() != null ? formato.format(m.getFecha()) : ""
            });
        }
    }//GEN-LAST:event_jbtn_listarActionPerformed

    private void jbtn_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_BuscarActionPerformed

        String idTexto = this.jtxt_idMinuta.getText().trim();

        if (idTexto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un ID para buscar", "Validación", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int idMinuta;
        try {
            idMinuta = Integer.parseInt(idTexto);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID inválido", "Validación", JOptionPane.WARNING_MESSAGE);
            this.jtxt_idMinuta.requestFocus();
            return;
        }

        MinutaPP reg = new MinutaPP();
        Minuta minuta = reg.buscarPorId(idMinuta);

        if (minuta != null) {
            this.jtxt_idMinuta.setText(String.valueOf(minuta.getIdMinuta()));
            this.jtxt_idMinuta.setEnabled(false);

            this.jcmb_tipo.setSelectedItem(minuta.getTipo());
            this.jtxt_nombre.setText(minuta.getNombre());
            this.jtxt_descripcion.setText(minuta.getDescripcion());

            Date fecha = minuta.getFecha();
            Calendar cal = Calendar.getInstance();
            cal.setTime(fecha);
            this.jtxt_dia.setText(String.valueOf(cal.get(Calendar.DAY_OF_MONTH)));
            this.jtxt_mes.setText(String.valueOf(cal.get(Calendar.MONTH) + 1));
            this.jtxt_agnio.setText(String.valueOf(cal.get(Calendar.YEAR)));
        } else {
            JOptionPane.showMessageDialog(this, "Minuta no encontrada", "Buscar", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jbtn_BuscarActionPerformed

