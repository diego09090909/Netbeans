    private void jbtn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_actualizarActionPerformed
        String id,nombre,tipo;
        int precioPlato;
        Date fechaCreacion;
        boolean disponible;
        String dia,mes,agnio;
        String fecha;
        id = this.jtxt_id.getText();
        nombre = this.jtxt_nombre.getText();
        tipo = this.jtxt_tipo.getText();
        
        dia = this.jtxt_dia.getText();
        mes = this.jtxt_mes.getText();
        agnio = this.jtxt_agnio.getText();
        
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        
        fecha = dia+"/"+mes+"/"+agnio;
        
        try {
            fechaCreacion = formato.parse(fecha);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Fecha de inicio inv치lida", "Validaci칩n", JOptionPane.WARNING_MESSAGE);
            return;
        }
        disponible = this.jchk_Disponible.isSelected();
        
       
        
       
        
        
        
    }//GEN-LAST:event_jbtn_actualizarActionPerformed

